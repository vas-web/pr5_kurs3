using System;

namespace Library
{
    public class Triad
    {
        public int a { get; set; }
        public int b { get; set; }
        public int c { get; set; }
        int aplusone()
        {
            return a + 1;
        }
        int bplusone()
        {
            return b + 1;
        }
        int cplusone()
        {
            return c + 1;
        }
    }
}
